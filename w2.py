jj = int(input("คุณรับมากี่กระบอก  : "))
pp1 = int(input("ราคารับมาคือ : "))
pp2 = int(input("ราคาขายไปคือ : "))
ss = int(input("คุณมีคนใช้งานกี่คน : "))
print("-----------------------สรุปผล-------------------")
sumprice = (jj * pp2)-(jj *pp1)
print (f"ต้นทุนทั้งหมดเท่ากับ{pp1 * jj}")
print(f"รายรับทั้งหมดคือ : {jj * pp2}")
print(f"กำไรคือ : {sumprice}")
print(f"บอสได้เงินไปทั้งคือ : {sumprice * 0.2}")
print(f"ลูกน้องได้ไปคนละ : {(sumprice * 0.8)/ss}")
