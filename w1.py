print ("รับสมัครสมาชิกจำนวน 5 อย่าง")
name = str(input("ชื่อของคุณคือ : "))
oo = int(input("กรอกอายุของคุณ : "))
tt = int(input("กรอกส่วนสูง : "))
pp = int(input("พละภายในคุณคือเท่าไหร่จาก 0-10 : "))
mm = int(input("เงินคุณมีเท่าไหร่มาที่นี้ : "))
if mm>10:
    print (f"คุณ  {name}  ผ่านการทดสอบเพราะว่าคุณ อายุ {oo} ถือว่าเกือบดี ส่วนสูง {tt} ชม. และแรงงานที่จะค่าพลังกาย {pp} ดีย์")
    print (f"โดยเฉพาะเงินจำนวน {mm} เราเอาหมด")
else:
    print("คุณไม่ผ่านคุณมันจน")